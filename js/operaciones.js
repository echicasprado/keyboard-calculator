/**
 * Funcion sumar
 * 
 * @param {float} numero1 
 * @param {float} numero2 
 * @returns {float} resultado de numero 1 con numero 2
 */
function sumar(numero1, numero2){
    return numero1 + numero2;
}

/**
 * Funcion restar 
 * 
 * @param {float} numero1 
 * @param {float} numero2 
 * @returns  resta de numero1 con numero2
 */
function resta(numero1,numero2){
    return numero1 - numero2;
}

/**
 * Funcion multiplicar
 * @param {float} numero1 
 * @param {float} numero2 
 * @returns multiplicacion de numero1 con numero2
 */
function multiplicacion(numero1,numero2){
    return numero1 * numero2;
}

/**
 * Funcion dividir
 * @param {float} numero1 
 * @param {float} numero2 
 * @returns division de numero1 con numero2
 */
function division(numero1, numero2){
    return numero1 / numero2;
}

/**
 * Funcion de potencia
 * @param {float} numero1 
 * @param {float} numero2 
 * @returns resultado de la potencia de numero1 con numero2
 */
function potencia(numero1,numero2){
    return Math.pow(numero1,numero2);
}